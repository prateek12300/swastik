export function getPujaById(id: string): PujaPackage | undefined {
  return pujaPackages.find((p) => p.id === id)
}

export function getPujasByCategory(category: string): PujaPackage[] {
  return pujaPackages.filter((p) => p.category === category)
}

export function getFeaturedPujas(): PujaPackage[] {
  return pujaPackages.filter((p) => p.featured)
}

export function searchPujas(query: string): PujaPackage[] {
  const lowercaseQuery = query.toLowerCase()
  return pujaPackages.filter(
    (p) =>
      p.name.toLowerCase().includes(lowercaseQuery) ||
      p.nameHindi.includes(query) ||
      p.description.toLowerCase().includes(lowercaseQuery) ||
      p.items.some((item) => item.name.toLowerCase().includes(lowercaseQuery))
  )
}
